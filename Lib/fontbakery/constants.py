# ANSI color codes for the helper logging class:
def color(bg, fg, bold=False):
    bold_bit = 0
    if bold:
        bold_bit = 1
    return ('\033[{};{};{}m'.format(bold_bit, bg, fg+10) + '{}\033[0m').format

